var height = 900;
var radius = 10;
var svg = d3.select('#chartArea').append('svg');
svg.attr('height', height + 'px')
    .attr('width', width + 'px')
    .style('pointer-events', 'all');

function g10(n) {
    var g10Colors = ["#3366cc", "#dc3912", "#ff9900", "#109618", "#990099", "#0099c6", "#dd4477", "#66aa00", "#b82e2e", "#316395", "#994499", "#22aa99", "#aaaa11", "#6633cc", "#e67300", "#8b0707", "#651067", "#329262", "#5574a6", "#3b3eac"];
    return g10Colors[n % g10Colors.length];
}

var force = d3.layout.force()
    .nodes(graph.nodes, function(d) {
        return d.id;
    })
    .links(graph.links, function(d) {
        return d.id;
    })
    .size([width, height])
    .linkDistance([50])
    .charge([-200])
    .start();

var links = svg.selectAll('.link')
    .data(graph.links, function(d) {
        return d.id;
    })
    .enter()
    .append('line')
    .attr('class', 'link')
    .attr('x1', function(d) {
        return d.source.x;
    })
    .attr('y1', function(d) {
        return d.source.y;
    })
    .attr('x2', function(d) {
        return d.target.x;
    })
    .attr('y2', function(d) {
        return d.target.y;
    });

var nodes = svg.selectAll('.node')
    .data(graph.nodes, function(d) {
        return d.id;
    })
    .enter()
    .append('circle')
    .attr('class', 'node')
    .attr('cx', function(d) {
        return d.x;
    })
    .attr('cy', function(d) {
        return d.y;
    })
    .attr('r', radius)
    .attr('fill', function(d) {
        return g10(d.class - 1);
    })
    .call(force.drag);


force.on('tick', function() {
    nodes.attr('cx', function(d) {
            return d.x;
        })
        .attr('cy', function(d) {
            return d.y;
        });

    links.attr('x1', function(d) {
            return d.source.x;
        })
        .attr('y1', function(d) {
            return d.source.y;
        })
        .attr('x2', function(d) {
            return d.target.x;
        })
        .attr('y2', function(d) {
            return d.target.y;
        });
});

var linkIndex = {};

graph.links.forEach(function(d) {
    linkIndex[d.source.id + '-' + d.target.id] = true;
});

var transitionDuration = 1000;

nodes.on('mouseover', function(d) {

    nodes.classed('node-active', function(o) {
        return linkIndex[o.id + '-' + d.id] || linkIndex[d.id + '-' + o.id];
    });

    d3.select(this)
        .classed('node-active', true);

    links.classed('link-active', function(o) {
        return o.source == d || o.target == d;
    });

    d3.select(this)
        .transition()
        .duration(transitionDuration)
        .attr('r', radius + 5);
});

nodes.on('mouseout', function() {

    nodes.classed('node-active', false);
    links.classed('link-active', false);

    d3.select(this)
        .transition()
        .duration(transitionDuration)
        .attr('r', radius)
        .attr('fill', function(d) {
            return g10(d.class - 1);
        });
});
